// jumper - the first real game content since 2012: a small jump-and-run.
//
// Boots the engine exactly like tools/player (SDL3 owns the window and event
// loop, Orkige::Engine renders into it via the externalWindowHandle path) and
// is the reference for how a game sits on top of the engine:
//
//   scene = data, app = behavior. samples/jumper/level1.oscene holds only
//   GameObjects + components (textured platform/crate meshes, static and
//   dynamic RigidBodies, the goal marker) - it opens, renders and edits in
//   the editor like any other scene. Everything player-specific (input,
//   jumping, respawn, win detection, camera) lives HERE, in JumperGame; the
//   app spawns the player object itself at runtime. A GameLogicComponent /
//   Lua-driven behavior layer that would move this INTO the scene is future
//   work (scripting).
//
// Assets: samples/jumper/media/*, generated by Util/make_jumper_assets.py
// (license-clean, stdlib-only). The PNG textures are EMBEDDED in the .glb
// binary chunks; OGRE's Codec_Assimp registers embedded images in the
// TextureManager and wires them into the imported materials, so no sidecar
// texture files are needed at runtime (the .png files exist for reference).
// All jumper meshes carry white COLOR_0, so the engine's shared unlit fix
// (vertex-colour tracking) leaves the textures intact.
//
// Gameplay tuning (the "feel" numbers, see JumperGame):
//   gravity (0,-20,0)  - ~2x earth, snappy platformer arcs
//   move speed 4.5 m/s - velocity-controlled (exponential approach, rate 12/s
//                        -> ~98% of target speed after 1/3 s), full air control
//   jump speed 8 m/s   - apex ~1.6 m after ~0.4 s, ~0.8 s total air time
//   kill plane y=-10   - falling below respawns at the start
//
// Controls: A/D or Left/Right move along the level (x), W/S or Up/Down dodge
// in depth (z - the level is 2.5D-ish but fully 3D, planar mode is OFF),
// SPACE jumps (grounded check via a short PhysicsWorld::castRay below the
// capsule), ESC quits. The capsule is kept upright by zeroing its angular
// velocity every frame (no rotation-lock DOFs are exposed yet).
//
// Modes / automation hooks:
//   jumper [scene.oscene]        play (default level: samples/jumper/level1.oscene)
//   jumper --write-level [path]  rebuild level1.oscene through SceneSerializer
//   ORKIGE_DEMO_FRAMES=N         exit 0 after N frames
//   ORKIGE_DEMO_SCREENSHOT=path  framebuffer dump at frame 55
//   ORKIGE_DEMO_WINDOW_SIZE=WxH  window size override (perf measurements)
//   ORKIGE_DEMO_FPS_LOG=1        log frame count / avg / p95 ms at exit
//   ORKIGE_JUMPER_SELFCHECK=1    scripted input run asserting textures loaded,
//                                movement, jump+landing, fall-respawn, the
//                                goal path and the fastgui HUD (widgets exist,
//                                title splash times out, win banner fires) -
//                                the jumper_selfcheck ctest test
//   ORKIGE_JUMPER_HUD_SCREENSHOT=path  (selfcheck only) framebuffer dump a few
//                                frames after the win, HUD banner visible
//
// HUD: engine_fastgui (the engine's own UiRenderer-based UI) with the generated
// atlas media/fastgui_default.{ogui,png} - see the shared JumperHud.h (also
// compiled by the jumper-native project module) and Util/make_fastgui_atlas.py.
