//! create one visible object: Transform + Model (+ optional RigidBody)
bool makeLevelObject(Orkige::GameObjectManager& gameObjectManager,
	std::string const& id, std::string const& mesh,
	Orkige::Vec3 const& position, Orkige::Vec3 const& scale,
	bool withBody, Orkige::PhysicsWorld::BodyType bodyType, float friction)
{
	optr<Orkige::GameObject> gameObject =
		gameObjectManager.createGameObject(id).lock();
	if (!gameObject ||
		!gameObject->addComponent<Orkige::TransformComponent>() ||
		!gameObject->addComponent<Orkige::ModelComponent>())
	{
		return false;
	}
	Orkige::TransformComponent* transform =
		gameObject->getComponentPtr<Orkige::TransformComponent>();
	transform->setPosition(position);
	transform->setScale(scale);
	gameObject->getComponentPtr<Orkige::ModelComponent>()->loadModel(mesh);
	if (withBody)
	{
		if (!gameObject->addComponent<Orkige::RigidBodyComponent>())
		{
			return false;
		}
		Orkige::RigidBodyComponent* rigidBody =
			gameObject->getComponentPtr<Orkige::RigidBodyComponent>();
		rigidBody->setBodyType(bodyType);
		// the meshes are unit cubes: collision halfExtents = scale * 0.5
		// (RigidBodyComponent does not follow the transform scale)
		rigidBody->setBoxShape(scale * 0.5f);
		rigidBody->setMass(1.0f);
		rigidBody->setFriction(friction);
		rigidBody->setRestitution(0.0f);
	}
	return true;
}

//! @brief build level1: 9 platforms rising along +x (gaps 1.25-2.0, rises up
//! to 1.0 - comfortably inside the 1.6m jump apex), crates to knock around
//! and the goal buddy at the end. Pure scene DATA - saved via SceneSerializer
//! and checked in; the jumper app itself only ever LOADS the .oscene.
bool buildLevel(Orkige::GameObjectManager& gameObjectManager)
{
	struct PlatformDef
	{
		const char* id;
		Orkige::Vec3 position;
		Orkige::Vec3 scale;
	};
	// top surface = position.y + scale.y / 2
	const PlatformDef platforms[] = {
		{ "Platform1", {  0.0f, -0.25f, 0.0f }, { 4.0f, 0.5f, 4.0f } },
		{ "Platform2", {  5.0f,  0.25f, 0.0f }, { 3.0f, 0.5f, 3.0f } },
		{ "Platform3", {  9.5f,  1.00f, 0.0f }, { 3.0f, 0.5f, 3.0f } },
		{ "Platform4", { 14.0f,  0.25f, 0.0f }, { 2.5f, 0.5f, 3.0f } },
		{ "Platform5", { 18.0f,  1.25f, 0.0f }, { 2.5f, 0.5f, 3.0f } },
		{ "Platform6", { 22.5f,  2.25f, 0.0f }, { 2.5f, 0.5f, 3.0f } },
		{ "Platform7", { 27.0f,  0.75f, 0.0f }, { 3.0f, 0.5f, 3.0f } },
		{ "Platform8", { 31.5f,  1.75f, 0.0f }, { 2.5f, 0.5f, 3.0f } },
		{ "Platform9", { 36.0f,  0.75f, 0.0f }, { 4.0f, 0.5f, 4.0f } },
	};
	for (PlatformDef const& platform : platforms)
	{
		if (!makeLevelObject(gameObjectManager, platform.id,
			"jumper_platform.glb", platform.position, platform.scale,
			true, Orkige::PhysicsWorld::BT_STATIC, 0.9f))
		{
			return false;
		}
	}
	// dynamic crates resting on platforms (top + 0.3)
	const PlatformDef crates[] = {
		{ "Crate1", {  9.5f, 1.55f,  0.9f }, { 0.6f, 0.6f, 0.6f } },
		{ "Crate2", { 27.0f, 1.30f, -0.7f }, { 0.6f, 0.6f, 0.6f } },
		{ "Crate3", { 27.0f, 1.90f, -0.7f }, { 0.6f, 0.6f, 0.6f } },
	};
	for (PlatformDef const& crate : crates)
	{
		if (!makeLevelObject(gameObjectManager, crate.id, "jumper_crate.glb",
			crate.position, crate.scale, true,
			Orkige::PhysicsWorld::BT_DYNAMIC, 0.6f))
		{
			return false;
		}
	}
	// the goal: a buddy capsule waiting on the last platform (no rigid body -
	// it is a marker; the app's win check reads its position)
	return makeLevelObject(gameObjectManager, "Goal", "jumper_player.glb",
		Orkige::Vec3(37.0f, 1.6f, 0.0f), Orkige::Vec3(1.0f, 1.0f, 1.0f),
		false, Orkige::PhysicsWorld::BT_STATIC, 0.0f);
}

//! does sub-mesh 0 of a ModelComponent's mesh sample a texture - 1/0, or -1
//! when there is no loaded model (the selfcheck's "the textures actually
//! loaded" probe, now through the facade MeshInstance)
int textureUnitCount(Orkige::GameObjectManager& gameObjectManager,
	std::string const& id)
{
	optr<Orkige::GameObject> gameObject =
		gameObjectManager.getGameObject(id).lock();
	if (!gameObject || !gameObject->hasComponent<Orkige::ModelComponent>())
	{
		return -1;
	}
	optr<Orkige::MeshInstance> mesh =
		gameObject->getComponentPtr<Orkige::ModelComponent>()
			->getMeshInstance();
	if (!mesh || mesh->getNumSubMeshes() == 0)
	{
		return -1;
	}
	return mesh->subMeshHasTexture(0) ? 1 : 0;
}

//! push a synthetic key event through the SDL queue (same pattern as the
//! demo's ORKIGE_DEMO_SYNTH_ESC) - the loop's SDL_PollEvent feeds it into
//! InputManager::injectEvent, so scripted input takes the REAL input path
void pushKeyEvent(SDL_Scancode scancode, SDL_Keycode key, bool down)
{
	SDL_Event event{};
	event.type = down ? SDL_EVENT_KEY_DOWN : SDL_EVENT_KEY_UP;
	event.key.scancode = scancode;
	event.key.key = key;
	event.key.down = down;
	SDL_PushEvent(&event);
}

