int main(int argc, char** argv)
{
	// argument handling: optional scene path, or --write-level [path]
	std::string scenePath = ORKIGE_JUMPER_LEVEL_FILE;
	bool writeLevel = false;
	for (int argIndex = 1; argIndex < argc; ++argIndex)
	{
		if (std::strcmp(argv[argIndex], "--write-level") == 0)
		{
			writeLevel = true;
		}
		else if (argv[argIndex][0] != '-')
		{
			scenePath = argv[argIndex];
		}
		else
		{
			SDL_Log("usage: jumper [scene.oscene] [--write-level]");
			return 1;
		}
	}

	if (!SDL_Init(SDL_INIT_VIDEO))
	{
		SDL_Log("SDL_Init failed: %s", SDL_GetError());
		return 1;
	}
	// ORKIGE_DEMO_WINDOW_SIZE=WxH: window size override for automated
	// performance measurements (default stays 1280x720)
	int windowWidth = 1280;
	int windowHeight = 720;
	if (const char* windowSize = std::getenv("ORKIGE_DEMO_WINDOW_SIZE"))
	{
		int width = 0;
		int height = 0;
		if (std::sscanf(windowSize, "%dx%d", &width, &height) == 2 &&
			width > 0 && height > 0)
		{
			windowWidth = width;
			windowHeight = height;
		}
	}
	SDL_Window* window = SDL_CreateWindow("Orkige Jumper",
		windowWidth, windowHeight, 0);
	if (!window)
	{
		SDL_Log("SDL_CreateWindow failed: %s", SDL_GetError());
		return 1;
	}
	// SDL may have clamped the request to the usable display bounds
	SDL_GetWindowSize(window, &windowWidth, &windowHeight);

	int exitCode = 0;
	{
		// engine singletons normally created by Orkige::Application; the same
		// boot set as the demo/player/editor
		Orkige::Timer::initialise();
		Orkige::GlobalEventManager eventManager;
		Orkige::ScriptRuntime scriptRuntime;
		init_module_orkige_core();

		// automation hooks (read before engine setup - they gate the vsync
		// and frame-pacing decisions below): ORKIGE_DEMO_FRAMES frame-limits
		// the run, ORKIGE_JUMPER_SELFCHECK runs the scripted verification
		unsigned long frameLimit = 0;
		if (const char* demoFrames = std::getenv("ORKIGE_DEMO_FRAMES"))
		{
			frameLimit = std::strtoul(demoFrames, nullptr, 10);
		}
		const bool selfCheck =
			(std::getenv("ORKIGE_JUMPER_SELFCHECK") != nullptr);
		// automated runs (ctest, screenshots, measurements) render as fast as
		// the machine allows; a HUMAN run gets vsync so the game neither spins
		// uncapped nor tears
		const bool automatedRun = selfCheck || frameLimit != 0;

		// ORKIGE_SANCTIONED_OGRE_BEGIN(classic-boot) - lint gate, see Util/ogre_containment.json
		// --- classic boot block (sanctioned raw-Ogre corner, see
		// Docs/render-abstraction.md "App boot"): Engine construction/config
		// and the RTSS-internal media registration stay classic plumbing;
		// everything after Engine::setup talks to the engine_render facade.
		Orkige::Engine engine(Ogre::SMT_DEFAULT,
			Orkige::StringUtil::BLANK, Orkige::StringUtil::BLANK,
			Orkige::StringUtil::BLANK, "jumper.log");
		engine.setCustomWindowParam("width",
			Orkige::StringUtil::Converter::toString(windowWidth));
		engine.setCustomWindowParam("height",
			Orkige::StringUtil::Converter::toString(windowHeight));
		if (!automatedRun)
		{
			engine.setCustomWindowParam("vsync", "true");
		}
		if (const char* renderSystemEnv = std::getenv("ORKIGE_RENDERSYSTEM"))
		{
			engine.setPreferredRenderSystem(renderSystemEnv);
		}

		// RTSS shader library (backend-internal media, needed before setup -
		// classic bootstrap business, not a facade call)
		Ogre::ResourceGroupManager::getSingleton().addResourceLocation(
			ORKIGE_JUMPER_MEDIA_DIR "/Main", "FileSystem", Ogre::RGN_INTERNAL);
		Ogre::ResourceGroupManager::getSingleton().addResourceLocation(
			ORKIGE_JUMPER_MEDIA_DIR "/RTShaderLib", "FileSystem",
			Ogre::RGN_INTERNAL);

		if (!engine.setup("Orkige Jumper", Orkige::Engine::SHOW_NEVER,
			Orkige::StringUtil::Converter::toString(
				reinterpret_cast<size_t>(orkige_native_window_handle(window)))))
		{
			SDL_Log("Engine::setup failed");
			return 1;
		}
		// ORKIGE_SANCTIONED_OGRE_END
		// --- end of the classic boot block: from here on the game talks to
		// the renderer through the engine_render facade exclusively
		Orkige::RenderSystem* render = Orkige::RenderSystem::get();
		Orkige::RenderWorld* world = render->getWorld();

		// the jumper assets (textured .glb meshes) through the facade
		render->addResourceLocation(ORKIGE_JUMPER_ASSET_DIR);
		render->initialiseResourceGroups();

		// the window camera on a facade rig (createDefaultCameraAndViewport
		// successor); the fixed yaw axis keeps the per-frame follow-camera
		// lookAt roll-free - the selfcheck asserts exactly that
		optr<Orkige::RenderCamera> camera = world->createCamera("jumper.camera");
		optr<Orkige::RenderNode> cameraNode =
			world->createNode("jumper.cameraNode");
		cameraNode->setFixedYawAxis(true);
		camera->attachTo(cameraNode);
		render->showCameraOnWindow(camera);

		world->setAmbientLight(Orkige::Color(0.2f, 0.2f, 0.2f));
		// a friendly sky instead of the void
		render->setWindowBackgroundColour(Orkige::Color(0.53f, 0.75f, 0.94f));

		// shared primitives scenes may reference (same as player/editor):
		// the procedural cube mesh + its unlit "VertexColour" material
		world->createVertexColourCubeMesh();

		// input pipeline: the poll loop feeds every SDL event into the
		// InputManager, which triggers the Orkige input events JumperInput
		// listens to - gameplay code never sees raw SDL
		Orkige::InputManager inputManager;
		JumperInput input;
		optr<Orkige::EventListener> keyPressedListener =
			Orkige::GlobalEventManager::getSingleton().bind(
				Orkige::InputManager::KeyPressedEvent,
				&JumperInput::onKeyPressed, &input);
		optr<Orkige::EventListener> keyReleasedListener =
			Orkige::GlobalEventManager::getSingleton().bind(
				Orkige::InputManager::KeyReleasedEvent,
				&JumperInput::onKeyReleased, &input);

		init_module_orkige_engine();
		Orkige::GameObjectManager gameObjectManager;
		Orkige::PhysicsWorld physicsWorld;

		if (writeLevel)
		{
			// generator mode: build the level DATA and save it through the
			// serializer - this is how samples/jumper/level1.oscene is made
			if (!buildLevel(gameObjectManager))
			{
				SDL_Log("jumper: FAILED - level construction");
				return 1;
			}
			if (!Orkige::SceneSerializer::saveScene(scenePath,
				gameObjectManager))
			{
				SDL_Log("jumper: FAILED - could not save '%s'",
					scenePath.c_str());
				return 1;
			}
			SDL_Log("jumper: level written to '%s' (%zu GameObjects)",
				scenePath.c_str(), gameObjectManager.getGameObjects().size());
			SDL_DestroyWindow(window);
			SDL_Quit();
			return 0;
		}

		if (!Orkige::SceneSerializer::loadScene(scenePath, gameObjectManager))
		{
			SDL_Log("jumper: FAILED - could not load scene '%s'",
				scenePath.c_str());
			return 1;
		}
		applyUnlitFixToLoadedModels(gameObjectManager);
		SDL_Log("jumper: level '%s' loaded (%zu GameObjects)",
			scenePath.c_str(), gameObjectManager.getGameObjects().size());

		if (!physicsWorld.init())
		{
			SDL_Log("jumper: FAILED - PhysicsWorld::init failed");
			return 1;
		}
		physicsWorld.setGravity(Orkige::Vec3(0.0f, JumperGame::GRAVITY_Y, 0.0f));

		JumperGame game(gameObjectManager, physicsWorld, cameraNode);
		if (!game.spawnPlayer())
		{
			SDL_Log("jumper: FAILED - player creation");
			return 1;
		}
		game.findGoal();
		cameraNode->setPosition(game.getSpawnPosition() +
			Orkige::Vec3(0.0f, 2.2f, 8.0f));
		cameraNode->lookAt(game.getSpawnPosition(),
			Orkige::RenderNode::TS_WORLD);

		// the HUD (engine_fastgui): title splash, controls hint, win banner,
		// distance-to-goal progress bar - see the shared JumperHud.h
		unsigned int hudWidth = 0;
		unsigned int hudHeight = 0;
		render->getWindowSize(hudWidth, hudHeight);
		Orkige::JumperHud hud(static_cast<int>(hudWidth),
			static_cast<int>(hudHeight));
		int hudLastWinCount = game.getWinCount();

		// frame-time statistics: the ORKIGE_DEMO_FPS_LOG measurement hook and
		// the one-time "this build is too slow to play" hint
		Orkige::FrameStatsUtil frameStats;

		// --- ORKIGE_JUMPER_SELFCHECK=1: scripted verification run ----------
		// Injects synthetic SDL key events (the demo's SYNTH_ESC pattern) so
		// every input goes through the real pipeline, then asserts:
		//   frame  5  the textured meshes loaded (texture units on the
		//             platform + player materials)
		//   10..30    hold RIGHT  -> frame 45: the player moved +x
		//   frame 60  press SPACE -> the player rises >0.8m and is grounded
		//             again within 120 frames
		//   then      teleport into the first gap -> falls below y=-10 ->
		//             respawns at the start
		//   then      teleport in front of the goal -> win fires -> respawn
		// Any missed deadline exits non-zero; measured values are logged.
		// (selfCheck itself is read above, before the engine window exists.)
		enum class CheckPhase
		{
			Script,			// fixed-frame part (textures, move, jump press)
			WaitLanding,	// jump flight until grounded again
			WaitRespawn,	// falling in the gap until the respawn happened
			WaitWin,		// walking distance of the goal until win fires
			WaitBanner,		// HUD: win banner visible + rendered (screenshot)
			Done
		};
		CheckPhase checkPhase = CheckPhase::Script;
		float checkStartX = 0.0f;
		float checkMovedX = 0.0f;
		float checkJumpStartY = 0.0f;
		float checkMaxRise = 0.0f;
		unsigned long checkJumpFrame = 0;
		unsigned long checkLandedFrame = 0;
		unsigned long checkPhaseDeadline = 0;
		int checkBaseRespawns = 0;
		int checkBaseWins = 0;
		float checkMaxProgress = 0.0f;	//!< peak HUD progress on the goal walk
		int checkProgressFrames = 0;	//!< frames with a near-full HUD bar
		bool checkFailed = false;
		auto checkFail = [&](std::string const& what)
		{
			SDL_Log("jumper: SELFCHECK FAILED - %s (frame dependent state: "
				"pos %.2f/%.2f/%.2f grounded=%d respawns=%d wins=%d)",
				what.c_str(), game.getPlayerPosition().x,
				game.getPlayerPosition().y, game.getPlayerPosition().z,
				static_cast<int>(game.isGrounded()), game.getRespawnCount(),
				game.getWinCount());
			checkFailed = true;
		};

		bool running = true;
		unsigned long frameCount = 0;
		std::chrono::steady_clock::time_point lastFrameTime =
			std::chrono::steady_clock::now();
		while (running)
		{
			SDL_Event event;
			while (SDL_PollEvent(&event))
			{
				if (event.type == SDL_EVENT_QUIT)
				{
					running = false;
				}
				inputManager.injectEvent(event);
			}
			if (input.quitRequested)
			{
				running = false;
			}

			// measured frame dt. Automated (frame-scripted) runs keep the
			// historical 1/60 floor so headless frames accumulate simulated
			// time; a HUMAN run uses the real dt - flooring it at 1/60 made
			// gameplay run FASTER than real time whenever rendering beat 60
			// fps (physics stepped its fixed 1/60 tick once per rendered
			// frame). The 0.1 cap stays: it avoids the catch-up spiral after
			// a stall, at the honest price of slow motion below 10 fps.
			const std::chrono::steady_clock::time_point frameTime =
				std::chrono::steady_clock::now();
			float deltaTime = std::chrono::duration<float>(
				frameTime - lastFrameTime).count();
			lastFrameTime = frameTime;
			frameStats.addFrame(deltaTime);
			frameStats.maybeWarnSlow("jumper");
			deltaTime = std::clamp(deltaTime,
				automatedRun ? 1.0f / 60.0f : 0.0001f, 0.1f);

			physicsWorld.update(deltaTime);
			gameObjectManager.update(deltaTime);	// body creation + pose sync
			game.update(deltaTime, input);			// the gameplay rules

			// HUD: win banner on a new win, then timers + progress bar
			if (game.getWinCount() > hudLastWinCount)
			{
				hudLastWinCount = game.getWinCount();
				hud.showWinBanner();
			}
			hud.update(deltaTime, game.getGoalProgress());

			if (!render->renderOneFrame())
			{
				running = false;
			}
			++frameCount;

			if (frameCount == 55)
			{
				// ORKIGE_DEMO_SCREENSHOT: the player stands mid-level on the
				// textured start platform, the level stretching to the right
				if (const char* shotPath = std::getenv("ORKIGE_DEMO_SCREENSHOT"))
				{
					render->saveWindowContents(shotPath);
				}
			}

			// --- selfcheck script ------------------------------------------
			if (selfCheck && !checkFailed && checkPhase == CheckPhase::Script)
			{
				if (frameCount == 5)
				{
					const int platformUnits =
						textureUnitCount(gameObjectManager, "Platform1");
					const int crateUnits =
						textureUnitCount(gameObjectManager, "Crate1");
					const int playerUnits =
						textureUnitCount(gameObjectManager, "Player");
					SDL_Log("jumper: selfcheck textures - platform=%d crate=%d "
						"player=%d texture units", platformUnits, crateUnits,
						playerUnits);
					if (platformUnits < 1 || crateUnits < 1 || playerUnits < 1)
					{
						checkFail("textured meshes did not load with textures");
					}
					// HUD boot: FastGuiManager loaded the generated atlas and
					// all four widgets exist; the title splash is showing
					SDL_Log("jumper: selfcheck hud - widgets=%d title=%d "
						"banner=%d progress=%.0f%%",
						static_cast<int>(hud.widgetsExist()),
						static_cast<int>(hud.isTitleVisible()),
						static_cast<int>(hud.isWinBannerVisible()),
						hud.getProgress());
					if (!hud.widgetsExist())
					{
						checkFail("HUD widgets missing after FastGuiManager boot");
					}
					else if (!hud.isTitleVisible())
					{
						checkFail("HUD title not visible at start");
					}
					else if (hud.isWinBannerVisible())
					{
						checkFail("HUD win banner visible before any win");
					}
				}
				if (frameCount == 10)
				{
					checkStartX = game.getPlayerPosition().x;
					pushKeyEvent(SDL_SCANCODE_RIGHT, SDLK_RIGHT, true);
				}
				if (frameCount == 30)
				{
					pushKeyEvent(SDL_SCANCODE_RIGHT, SDLK_RIGHT, false);
				}
				if (frameCount == 45)
				{
					checkMovedX = game.getPlayerPosition().x - checkStartX;
					SDL_Log("jumper: selfcheck move - +x %.3f m after 20 "
						"frames of RIGHT (target speed %.1f m/s)",
						checkMovedX, JumperGame::MOVE_SPEED);
					if (checkMovedX < 0.6f)
					{
						checkFail("player did not move right under input");
					}
				}
				if (frameCount == 60)
				{
					if (!game.isGrounded())
					{
						checkFail("player not grounded before the jump");
					}
					checkJumpStartY = game.getPlayerPosition().y;
					checkJumpFrame = frameCount;
					checkMaxRise = 0.0f;
					pushKeyEvent(SDL_SCANCODE_SPACE, SDLK_SPACE, true);
					checkPhase = CheckPhase::WaitLanding;
					checkPhaseDeadline = frameCount + 120;
				}
			}
			else if (selfCheck && !checkFailed &&
				checkPhase == CheckPhase::WaitLanding)
			{
				checkMaxRise = std::max(checkMaxRise,
					game.getPlayerPosition().y - checkJumpStartY);
				if (frameCount == checkJumpFrame + 5)
				{
					pushKeyEvent(SDL_SCANCODE_SPACE, SDLK_SPACE, false);
				}
				// give take-off a few frames before accepting "grounded again"
				if (frameCount > checkJumpFrame + 10 && game.isGrounded())
				{
					checkLandedFrame = frameCount;
					SDL_Log("jumper: selfcheck jump - rise %.3f m (apex target "
						"%.2f), landed %lu frames after take-off",
						checkMaxRise, JumperGame::JUMP_SPEED *
						JumperGame::JUMP_SPEED / (2.0f * -JumperGame::GRAVITY_Y),
						checkLandedFrame - checkJumpFrame);
					if (checkMaxRise < 0.8f)
					{
						checkFail("jump did not raise the player");
					}
					else
					{
						// drop the player into the first gap: no ground below,
						// it must fall past the kill plane and respawn
						checkBaseRespawns = game.getRespawnCount();
						game.teleportPlayer(Orkige::Vec3(2.75f, 1.5f, 0.0f));
						checkPhase = CheckPhase::WaitRespawn;
						checkPhaseDeadline = frameCount + 300;
					}
				}
				else if (frameCount >= checkPhaseDeadline)
				{
					checkFail("player never landed after the jump");
				}
			}
			else if (selfCheck && !checkFailed &&
				checkPhase == CheckPhase::WaitRespawn)
			{
				if (game.getRespawnCount() > checkBaseRespawns)
				{
					const float spawnDistance = game.getPlayerPosition()
						.distance(game.getSpawnPosition());
					SDL_Log("jumper: selfcheck respawn - fell off, back %.3f m "
						"from the spawn point after respawn #%d",
						spawnDistance, game.getRespawnCount());
					if (spawnDistance > 1.0f)
					{
						checkFail("respawn did not return to the start");
					}
					else
					{
						// walk-in test of the win path: drop just before the
						// buddy on the goal platform
						checkBaseWins = game.getWinCount();
						game.teleportPlayer(Orkige::Vec3(36.0f, 3.0f, 0.0f));
						checkPhase = CheckPhase::WaitWin;
						checkPhaseDeadline = frameCount + 300;
					}
				}
				else if (frameCount >= checkPhaseDeadline)
				{
					checkFail("falling off never triggered a respawn");
				}
			}
			else if (selfCheck && !checkFailed &&
				checkPhase == CheckPhase::WaitWin)
			{
				// standing right in front of the goal, the HUD progress bar
				// must be near-full (screenshot the third such frame - the
				// framebuffer read can lag the simulation by one frame)
				if (hud.getProgress() > 80.0f)
				{
					++checkProgressFrames;
					if (checkProgressFrames == 3)
					{
						if (const char* progressShotPath = std::getenv(
							"ORKIGE_JUMPER_HUD_PROGRESS_SCREENSHOT"))
						{
							render->saveWindowContents(progressShotPath);
						}
					}
				}
				checkMaxProgress = std::max(checkMaxProgress,
					hud.getProgress());
				if (game.getWinCount() > checkBaseWins)
				{
					if (checkMaxProgress <= 80.0f)
					{
						checkFail("HUD progress bar never approached full "
							"in front of the goal");
					}
					// HUD: the win must have raised the banner this frame (the
					// hud update runs right after game.update); the progress
					// bar must have been near-full walking into the goal and
					// the title splash must be long gone (3s < the win frame)
					SDL_Log("jumper: selfcheck hud win - banner=%d title=%d",
						static_cast<int>(hud.isWinBannerVisible()),
						static_cast<int>(hud.isTitleVisible()));
					if (!hud.isWinBannerVisible())
					{
						checkFail("win did not show the HUD win banner");
					}
					else if (hud.isTitleVisible())
					{
						checkFail("HUD title splash never timed out");
					}
					else
					{
						checkPhase = CheckPhase::WaitBanner;
						checkPhaseDeadline = frameCount + 5;
					}
				}
				else if (frameCount >= checkPhaseDeadline)
				{
					checkFail("reaching the goal never triggered the win");
				}
			}
			else if (selfCheck && !checkFailed &&
				checkPhase == CheckPhase::WaitBanner)
			{
				// give the banner a few rendered frames, then optionally dump
				// a screenshot with the HUD visible and finish
				if (frameCount >= checkPhaseDeadline)
				{
					if (!hud.isWinBannerVisible())
					{
						checkFail("HUD win banner disappeared too early");
					}
					else
					{
						if (const char* hudShotPath =
							std::getenv("ORKIGE_JUMPER_HUD_SCREENSHOT"))
						{
							render->saveWindowContents(hudShotPath);
							SDL_Log("jumper: selfcheck hud screenshot -> %s",
								hudShotPath);
						}
						// camera-roll regression: the follow camera lookAt's
						// every frame of this whole session (plus respawn/
						// teleport jumps) - the engine's fixed yaw axis on
						// the camera node must have kept it roll-free (the
						// old shortest-arc lookAt slowly tilted the horizon)
						const float cameraRollDegrees = std::abs(
							cameraNode->getOrientation().getRoll()
								.valueDegrees());
						SDL_Log("jumper: selfcheck camera roll %.4f deg "
							"after the play session", cameraRollDegrees);
						if (cameraRollDegrees > 0.1f)
						{
							checkFail("follow camera accumulated roll");
						}
						else
						{
							SDL_Log("jumper: selfcheck complete - textures, "
								"movement (+%.2f m), jump (+%.2f m, %lu frames "
								"airborne), fall-respawn, the goal, the "
								"fastgui HUD and the roll-free camera all "
								"verified",
								checkMovedX, checkMaxRise,
								checkLandedFrame - checkJumpFrame);
							checkPhase = CheckPhase::Done;
							running = false;
						}
					}
				}
			}
			if (checkFailed)
			{
				exitCode = 1;
				running = false;
			}

			if (frameLimit != 0 && frameCount >= frameLimit)
			{
				running = false;
			}
		}

		if (selfCheck && !checkFailed && checkPhase != CheckPhase::Done)
		{
			SDL_Log("jumper: SELFCHECK FAILED - run ended in phase %d",
				static_cast<int>(checkPhase));
			exitCode = 1;
		}
		frameStats.logAtExit("jumper");
	}

	SDL_DestroyWindow(window);
	SDL_Quit();
	return exitCode;
}
